package com.katspow.caatja.math;

public class Dimension {
    
    /**
     * Width dimension.
     */
    public double width;
    
    /**
     * Height dimension.
     */
    public double height;
    
    public Dimension() {
        
    }

    public Dimension(double w, double h) {
        this.width = w;
        this.height = h;
    }
    
}

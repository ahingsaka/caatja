public class FontData {
    
    public FontData() {
        
    }
    
    public FontData(double height, double ascent, double descent) {
        super();
        this.height = height;
        this.ascent = ascent;
        this.descent = descent;
    }
    public double height;
    public double ascent;
    public double descent;

}
